package practise;

public class operatordemo {
 public static void main(String[]args) {
	 
	 
	 // Arithmetic +,-,*,/,%
	 //bitwise
	 //relational
	 //logical
	 
	 
	 int m= 6, n=4;
	 int r1 = m+n; // 8
	 int r2 = m-n;//4
	 int r3 = m *n;//12
	 double r4 = (double)m/n; //3
	 
	 int r5 = m%n;
	 
	 
	 int k = 4;
	 int b = 5;
	 
	 b += k; // b = k + b;
	 
	 ++k; // pre increment
	 k++; // k + 1; // post increment // short hand operators
	 k--; // k - 1; // decrement operator
	 
	
	
	 
	 System.out.println(k);
	 
	 
	 System.out.println(r1);
	 System.out.println(r2);
	 System.out.println(r3);
	 System.out.println(r4);
	 System.out.println(r5);
	 
	 
	 
 }
}
